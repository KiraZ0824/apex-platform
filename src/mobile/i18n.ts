export function getInvitationStatusLabel(status: string, lang: Language) {
  const labels = {
    zh: {
      ongoing: '进行中',
      pending: '待审核',
      completed: '已完成',
      cancelled: '已取消',
      open: '开放',
    },
    en: {
      ongoing: 'Ongoing',
      pending: 'Pending',
      completed: 'Completed',
      cancelled: 'Cancelled',
      open: 'Open',
    },
  };

  return labels[lang][status as keyof (typeof labels)[Language]] ?? status;
}

